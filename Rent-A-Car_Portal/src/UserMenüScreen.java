    private void submit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_buttonActionPerformed
        ArrayList<User> user_list = MainScreen.getUser_list();
        String user_ıd_text = new String(user_ıd_control_text.getText().trim());
        
        for (int i = 0; i < user_list.size() ; i++) {
            if (user_ıd_text.equals(user_list.get(i).getUser_Id())) {
                name_text.setText(user_list.get(i).getUser_name()) ;
                user_adress_text.setText(user_list.get(i).getHome_adress());
                user_tel_text.setText(user_list.get(i).getUser_mobil_no());
                String user_credit = Integer.toString(user_list.get(i).getUser_credit());
                user_credit_text.setText(user_credit);
                break;
            }
        }
    }//GEN-LAST:event_submit_buttonActionPerformed
    
    private void add_credit_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                                  
        
        
        ArrayList<User> user_list = MainScreen.getUser_list();
        Integer user_credit_add = Integer.parseInt(user_credit_add_text.getText());
        String user_ıd_text = new String(user_ıd_control_text.getText().trim());
        
            for (int i = 0; i < user_list.size() ; i++) { 

               if (user_ıd_text.equals(user_list.get(i).getUser_Id())) {
                   JOptionPane.showMessageDialog(this," ADDED SUCESSFULLY YOUR NEW CREDIT IS " + (user_list.get(i).getUser_credit() + user_credit_add) + " $" );
                   user_credit_text.setText(Integer.toString((user_list.get(i).getUser_credit() + user_credit_add)));
                   user_list.get(i).setUser_credit((user_list.get(i).getUser_credit() + user_credit_add));
               }
            }
    }
    
    private void rent_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rent_buttonActionPerformed
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        int choosen_row = car_info_table.getSelectedRow();
        ArrayList<Car> car_list = MainScreen.getCars();
        ArrayList<User> user_list = MainScreen.getUser_list();
            Integer day_num = Integer.parseInt(day_number_text.getText().trim());
            Integer price_perday = Integer.parseInt(car_price_text.getText().trim());
            int total_price = day_num * price_perday ;             
            String user_ıd_text = new String(user_ıd_control_text.getText().trim());
            

        if (car_avalibility_text.getText().toUpperCase().equals("YES")) {
            for (User user : user_list) {
                
               if (user_ıd_text.equals(user.getUser_Id())) {
                   
                   if (user.getUser_credit() < total_price){
                       JOptionPane.showMessageDialog(this," You dont have enough credit turn to user screen and please add credit ");
                       break;
                   }
                   else{
                       for (int i = 0; i < car_list.size() ; i++) {
                           if (car_list.get(choosen_row).getCar_ıd().equals(car_list.get(i).getCar_ıd())) {
                               JOptionPane.showMessageDialog(this," rent is successfully your credit : " + (user.getUser_credit() - total_price));
                               user.setUser_credit((user.getUser_credit() - total_price));
                               car_list.get(i).setAvailable(false);
                               car_avalibility_text.setText("NO");
                               model.setValueAt("NO",choosen_row ,5);
                           }
                           
                       }
                   }
               }
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"This car is not availability choose available car ");
        }
            
             
            
    }//GEN-LAST:event_rent_buttonActionPerformed

