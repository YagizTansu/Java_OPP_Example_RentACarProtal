    private void show_car_listActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_car_listActionPerformed
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        ArrayList<Car> car_list = MainScreen.getCars() ;
        String car_available_text = "";
        
        
        
        for(int i = 0 ; i < car_list.size() ; i++){
            
            if (car_list.get(i).getAvailable() == true) {
            car_available_text = "YES" ;
        }
        else{
            car_available_text = "NO" ;
        }
            
            model.addRow(new Object[]{car_list.get(i).getBrand(),car_list.get(i).getModel(),car_list.get(i).getColor(),car_list.get(i).getMotor_power(),car_list.get(i).getDaily_rent_price(),car_available_text});
        }
    }//GEN-LAST:event_show_car_listActionPerformed

