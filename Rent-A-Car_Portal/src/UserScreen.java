    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        UserMen端Screen usermen端screen = new  UserMen端Screen();
        String user_name = user_name_text.getText();
        String password = new String(user_password_text.getPassword());
        ArrayList<User> user_list =  MainScreen.getUser_list() ;
        User user_login ;
        if (user_list.size() == 0 ) {
            JOptionPane.showMessageDialog(this,"INVALID USER...");
        }
        else{
            for (User user : user_list) {
                if(user.getUser_name().equals(user_name) && user.getPassword().equals(password)){
                    JOptionPane.showMessageDialog(this,"LOGIN IS SUCCESSFUL" + user_name.toUpperCase());
                    usermen端screen.setVisible(true);
                    user_login = user ;
                }
                
                else{
                    JOptionPane.showMessageDialog(this,"WRONG USER NAME OR PASSWORD");
                }
            }
        }    
    }//GEN-LAST:event_loginActionPerformed

