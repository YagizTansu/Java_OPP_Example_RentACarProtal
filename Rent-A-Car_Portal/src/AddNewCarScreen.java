    private void add_new_car_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_new_car_buttonActionPerformed
        ArrayList<Car> car_list = MainScreen.getCars() ;
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        int id = car_list.size()+1 ;
        
        String  ID = new String(Integer.toString(id));        
        String brand = new String(car_brand_text.getText());
        String car_model = new String(car_model_text.getText());
        String  color = new String(car_color_text.getText());
        String motor_power = new String(car_motorpower_text.getText());
        Integer rent_price = new Integer(car_price_text.getText());

        Boolean available ;
        if (car_availabilty.getSelectedItem().equals("YES")) {
            available = true ;
        }
        else{
            available = false ;
        }
        
        car_list.add(new Car(ID,brand,car_model,color,motor_power,rent_price,available));
        model.addRow(new Object[]{ID,brand,car_model,color,motor_power,rent_price,car_availabilty.getSelectedItem().toString()});
        JOptionPane.showMessageDialog(this,"CAR HAS ADDED ");
    }//GEN-LAST:event_add_new_car_buttonActionPerformed

