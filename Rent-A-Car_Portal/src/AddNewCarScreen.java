    private void edıt_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edıt_buttonActionPerformed
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        ArrayList<Car> car_list = MainScreen.getCars() ;
        
        int choosen_row = car_info_table.getSelectedRow();

        if (choosen_row == -1) {
            if (car_info_table.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this,"Car table is empty ");
            }else{
                JOptionPane.showMessageDialog(this," Please choose a car to edit " );
            }
        }
        else{
        String brand = new String(car_brand_text.getText());
        String car_model = new String(car_model_text.getText());
        String  color = new String(car_color_text.getText());
        String motor_power = new String(car_motorpower_text.getText());
        Integer rent_price = new Integer(car_price_text.getText());
           
        Car edit_car = new Car(brand,car_model,color,motor_power,rent_price);
            
           model.setValueAt(brand,choosen_row ,0);
           model.setValueAt(car_model,choosen_row ,1);
           model.setValueAt(color ,choosen_row ,2);
           model.setValueAt(motor_power,choosen_row ,3);
           model.setValueAt(rent_price ,choosen_row ,4);
           
           
           
           car_list.set(choosen_row , edit_car);
           
        }
        

        
    }//GEN-LAST:event_edıt_buttonActionPerformed

    private void showbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showbuttonActionPerformed
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        ArrayList<Car> car_list = MainScreen.getCars() ;
        
        
        for(int i = 0 ; i < car_list.size() ; i++){
            model.addRow(new Object[]{car_list.get(i).getCar_ıd(),car_list.get(i).getBrand(),car_list.get(i).getModel(),car_list.get(i).getColor(),car_list.get(i).getMotor_power(),car_list.get(i).getDaily_rent_price(),car_list.get(i).getAvailable()});
        }
    }//GEN-LAST:event_showbuttonActionPerformed

    private void deletebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebuttonActionPerformed
        DefaultTableModel model = (DefaultTableModel)car_info_table.getModel();
        ArrayList<Car> car_list = MainScreen.getCars() ;
        
        int choosen_row = car_info_table.getSelectedRow();

        if (choosen_row == -1) {
            if (car_info_table.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this,"Car table is empty ");
            }else{
                JOptionPane.showMessageDialog(this,"Please choose a car to remove from list " );
            }
        }else{
            for (int i = 0; i < car_list.size() ; i++) {
                if (car_list.get(choosen_row).getCar_ıd().equals(car_list.get(i).getCar_ıd())) {
                    model.removeRow(choosen_row);
                    car_list.remove(i);
                    JOptionPane.showMessageDialog(this,"CAR HAS DELETED" );
                    break;
                }
            }  
        }
    }//GEN-LAST:event_deletebuttonActionPerformed

